int main(void)
{
  uint8_t ch = 0;
  EncoderApp_Sample_t encoder_sample = {0};

  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_I2C1_Init();
  MX_I2C3_Init();
  MX_FDCAN1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  MX_TIM8_Init();

  BSP_LED_Init(LED_GREEN);
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  BspCOMInit.BaudRate   = 115200;
  BspCOMInit.WordLength = COM_WORDLENGTH_8B;
  BspCOMInit.StopBits   = COM_STOPBITS_1;
  BspCOMInit.Parity     = COM_PARITY_NONE;
  BspCOMInit.HwFlowCtl  = COM_HWCONTROL_NONE;

  if (BSP_COM_Init(COM1, &BspCOMInit) != BSP_ERROR_NONE)
  {
    Error_Handler();
  }

  RobotApp_Init(&hi2c1, &htim1, &htim2);
  HAL_Delay(200);
  RobotApp_PrintHelp();
  CAN_App_Init();

  EncoderApp_Init(&htim5, &htim8, &htim4, &htim3);
  if (EncoderApp_Start() != HAL_OK)
  {
    Error_Handler();
  }

  while (1)
  {
    if (HAL_UART_Receive(&hcom_uart[COM1], &ch, 1, 1U) == HAL_OK)
    {
      RobotApp_HandleChar(ch);
    }

    if (EncoderApp_Poll(HAL_GetTick(), &encoder_sample) != 0U)
    {
      EncoderApp_PrintDebug(&encoder_sample);
    }
  }
}

